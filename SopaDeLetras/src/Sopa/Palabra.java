package Sopa;

public class Palabra {
    public String palabra;
    public int posx;
    public int posy;
    public int direccion;
    
    public Palabra(String palabra, int posx, int posy, int direccion) {
        this.palabra = palabra;
        this.posx = posx;
        this.posy = posy;
        this.direccion = direccion;
    }
    
}
